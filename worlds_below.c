const int MAX_ENTITY_COUNT = 1000;
const int MAX_HEALTH = 100;
const int NANO_SECONDS_PER_SECOND = 1000000000;

static SDL_Texture *texture = NULL;
static TTF_Font *font = NULL;

extern unsigned char tiny_ttf[];
